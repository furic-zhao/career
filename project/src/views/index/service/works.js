var works = {
	getList: function() {
		var listData = [];
		return Q.Promise(function(resolve, reject) {

			workInfo.data.map(function(item) {
				var itemData = {};
				itemData.id = item.id;
				itemData.name = item.name;
				itemData.url = item.url || "";
				itemData.tip = item.tip || "";
				itemData.desc = item.desc || "";
				itemData.cover = item.cover || (item.list ? item.list[0].url : "");

				listData.push(itemData);
			});
			resolve(listData);
		});
	},
	getTypeList: function(type) {
		var listData = [];
		return Q.Promise(function(resolve, reject) {

			workInfo.data.map(function(item) {
				if (type == item.type) {
					var itemData = {};
					itemData.id = item.id;
					itemData.name = item.name;
					itemData.url = item.url || "";
					itemData.tip = item.tip || "";
					itemData.desc = item.desc || "";
					itemData.cover = item.cover || (item.list ? item.list[0].url : "");

					listData.push(itemData);
				}
			});
			resolve(listData);
		});
	},
	getById: function(id) {
		var itemData = {};
		return Q.Promise(function(resolve, reject) {
			workInfo.data.map(function(item) {
				if (id == item.id) {
					itemData = item;
				}
			});
			resolve(itemData);
		});
	}
};

